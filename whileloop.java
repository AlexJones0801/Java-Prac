//Q13 Write a program for while loop.
public class whileloop{
   public static void main(String args[]){
      int num = 10; //Strating number
      
      //Print numbers from 10 to 1.
      while(num > 0){
         System.out.println(num);
         num--; //Decrementing num by 1 in each iteration
      }
   }
}